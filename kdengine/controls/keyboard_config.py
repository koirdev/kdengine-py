import pygame

pygame.init()

# Keyboard controls config.

# ---- Main keys ----

# Quit from game.
quit_from_game_keyboard = pygame.K_q # Q.

# Take a screenshot.
screenshot_keyboard = pygame.K_F7 # F7.

back_keyboard = pygame.K_BACKSPACE

# ---- Main Menu ----

# Up to menu section.
up_menu_section_keyboard = pygame.K_UP # Arrow up.

# Down to menu section.
down_menu_section_keyboard = pygame.K_DOWN # Arrow down.

# Select the menu section.
enter_keyboard = pygame.K_RETURN


# ---- Game Scene ---- 

# Player run.
player_run_keyboard = pygame.K_LSHIFT # Left Shift.

# Player go left.
player_go_left_keyboard = pygame.K_LEFT # Left Arrow.

# Player go right.
player_go_right_keyboard = pygame.K_RIGHT # Right Arrow.


